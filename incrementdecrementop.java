
public class incrementdecrementop {
    public static void main(String[] args) {
        //increment operator
        int i=6;
        int j=i++;//in this increment operator firstly value of i used than it will be increment
        System.out.println(j);
        System.out.println(i);
        //increment operator type 2
        int p=9;
        int q=++p;//in this type first value of p increased and than assigned to the p
        System.out.println(q);


        //same logic can be use for decrement operators




    }
}
