public class loops {
    public static void main(String[] args) {
        int a=1;


        while(a<=100)
        {
        System.out.println(a);
        a++;
        }

        //do while loop
        int b=0 ;
        do{
            System.out.println(b);
            b++;
        }while(b<9); 
         //                      For loop                          //   
         for(int z=0;z<=100;z+=2){
         System.out.println(z);
        }
        //                  Decremanting for loops                //
        for(int m=50;m>0;m--){
        System.out.println(m);
        }
    }   

    
}
